package com.saurabhjava;

public class pattern2 {
    public static void main(String[] args) {
        for (int i = 5; i >= 1; i--) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
        for (int i = 2; i <=5; i++) {
            for (int j = 1 ; j <=i ; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
